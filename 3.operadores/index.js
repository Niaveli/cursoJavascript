//CONCEPTO Y LOS TIPOS DE OPERADORES:
//OPERADORES: Los operadores permiten manipular el valor de las variables, 
//realizar operaciones matemáticas con sus valores y comparar diferentes variables.
// De esta forma, los operadores permiten a los programas realizar cálculos complejos
// y tomar decisiones lógicas en función de comparaciones y otros tipos de condiciones.
//TIPOS DE OPERADORES:
//Operadores aritméticos: Se utilizan para efectuar operaciones matemáticas.
 //Para realizar las operaciones básicas, como suma, resta, multiplicación y división.
//Operador de Asignación: Un operador de asignación asigna un valor a su operando
 // izquierdo basándose en el valor de su operando derecho, es decir el operador de asignación (=) 
 //consiste en asignar un valor a una variable.
//Operadores de Comparación: Se utilizan para comparar operandos y devuelven un valor lógico 
 //(verdadero o falso) sobre la base de la comparación.
//Operadores Lógicos: Los operadores lógicos se utilizan
 //normalmente con valores booleanos (lógicos).
//Operadores Bitwise: Son operadopres que actuan sobre números enteros pero usando
 //su representación binaria.
//Operadores especiales: Determina si un valor es nulo o no nulo, se usa para devolver 
 //un resutado true o false
//Operadores de Cadenas String:El operador de concatenación (+) se usa para concatenar (agregar) 
 //cadenas de texto o también llamados strings.

